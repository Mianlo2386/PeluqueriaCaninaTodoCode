    private void cargarTabla() {
        
        DefaultTableModel modeloTabla;
        modeloTabla = new DefaultTableModel(){
            
            //función para que filas y columnas no sean editables
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };   
        
        //Establecemos nombres para las columnas
        String titulos[] = {"Num", "Nombre", "Color", "Raza", "Alergico", "At. Especial", "Dueño", "Celular"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        //Carga de datos en la BD
        List <Mascota> listaMascotas = control.traerMascotas();
        
        //recorrer y mostrar lista
        
        if(listaMascotas != null) {
            for(Mascota masco : listaMascotas){
                Object[] objeto = {masco.getNumCliente(), masco.getNombre(), masco.getColor(),
                masco.getRaza(), masco.getAlergico(), masco.getAtencionEspecial(),
                masco.getDuenio().getNombre(), masco.getDuenio().getCelDuenio()};
                
                modeloTabla.addRow(objeto);
            }
        }
        //para signar a la igu de la tabla
        tablaMascotas.setModel(modeloTabla);
    }
